void main() {
  // Langkah 1

  // buat var counter berisi 30
  int counter = 30;

  // selama nilai var counter kurang dari 33
  while (counter < 33) {
    // maka tampilkan isi nilai dari var counter
    print(counter);
    // nilai var counter ditambah 1
    counter++;
  }

  // lakukan perintah
  do {
    // tampilkan nilai dari var counter
    print(counter);
    // nilai var counter ditambah 1
    counter++;
    // selama nilai dari var counter < 77
  } while (counter < 77);
}
